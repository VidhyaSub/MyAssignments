package week1.day2;

public class Calculator {
	public int addThreeNumbers (int a, int b, int c) {
		int sum =a+b+c;
		return sum;
	}
	public int subTwoNumbers (int e, int f) {
		int sub =e-f;
		return sub;
	}
	public int multiTwoNumbers (int h, int i) {
		int mul =h*i;
		return mul;
	}
	public int divTwoNumbers (int k, int l) {
		int div =k/l;
		return div;
	}	 
}
